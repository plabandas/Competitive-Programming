#include <bits/stdc++.h>
using namespace std;

int main()
{

    int n, i;
    cout << "Enter a number :: ";
    cin >> n;

    for (i = 1; i <= n; i = i + 1)
    {
        cout << i << " ";
    }

    cout << "\nThis is for while code"; // this code will make a new line in this code
    cout << "\n";

    int j = 1;

    while (j <= n)
    {
        cout << j << " ";
        j = j + 1;
    }
}
